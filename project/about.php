	<h3>Founded through Controversy</h3>
	<ul>
		<li>Novermber 4, 1897, the Supreme Court of Illinois v. Henry M. Day, et at.</li>
		<li>Current law students formed the "Law Student League" to fight the act then decided to solidify the bond by
		founding Lambda Epsilon Fraternity in 1898.</li>
		<li>Lambda representing Law, Epsilon representing Equality.</li>
		<li>Finally held a convention at the Colonial Tavern in South Haven, Michigan on July 16, 1902 unanimously adopted a resolution
		dissolving  Lambda Epsilon.</li>
		<li>On July 17, 1902 the South Haven Articles were signed which were to become the foundation of Phi Alpha Delta.</li>
		<li>The first four chapters were Blackstone, Story, Fuller and Webster & Marshall</li>
	</ul>
	<h3>Phi Alpha Delta Since the Beginning</h3>
		<ul><li>The First Coed Law Fraternity</li>
			<ul>
				<li>In 1970, Phi Alpha Delta began to admit women</li>
				<li>In 1972, it merged with Phi Delta Delta, the leading law sorority</li>
			</ul>
		</ul>
		<ul><li>The first Pre-Law Fraternity Program</li>
			<ul>
				<li>The 1980 convention saw the adoption of the Phi Alpha Delta Law Fraternity</li>
				<li>First Pre-Law Chapter chartered at Southwest Missouri State in 1981</li>
			</ul>
		</ul>
		<ul><li>Presence around the world</li>
			<ul>
				<li>Over 200 Pre-Law Chapters</li>
				<li>183 Law School Chapters at over 150 ABA accredited schools</li>
				<li>95 alumni chapters in the U.S., Mexico, Canada, and Puerto Rico</li>
				<li>Only law fraternity allotted funds from the Departement of Justice</li>
				<li>1 in 6 lawyers is a card-carrying Phi Alpha Delta member</li>
			</ul>
		</ul>
		<ul><li>The Phi Alpha Delta name</li>
			<ul>
				<li>The name connotes Philos Adelphos Dikaios, which means "Justice is a dear brother"
				<br/> or "Love of Humanity and Justice for all"</li>
			</ul>
		</ul>
		<ul><li>Phi Alpha Delta crest</li>
			<ul>
				<li>The book of law: immortal and undying spirit which is in truth the law - to which all
				precedents <br/> must bow and conform</li>
				<li>Lamp of learning: only through great industry and application will true learning be acquired</li>
				<li>Scales of justice: impartiality of the rule of law, which gives to every individual true justice,
				<br/> without favor or distiction</li>
				<li>Scroll: has the name of the fraternity on it</li>
			</ul>
		</ul>
			
			
